#include "SivInput.h"


//-------------------------------------------------------------------------------------------------
// 定義
//-------------------------------------------------------------------------------------------------
namespace GAME
{
	bool SivInput::IsJoyButton ( int id, int btn ) const
	{
		return m_gamepad.IsButton ( id, btn );
	}

	bool SivInput::IsAxisUp(int id) const
	{
		return m_gamepad.IsAxisUp ( id );
	}


}

