namespace GAME
{
	//■=======================================================================
	// ゲームグラフィック　ベース
	//■=======================================================================
	GameGraphicBase::GameGraphicBase ()
	{
	}

	GameGraphicBase::~GameGraphicBase ()
	{
	}

	void GameGraphicBase::Load ()
	{
		m_tx = std::make_shared < s3d::Texture > ( m_filename );
	}

	void GameGraphicBase::Draw ()
	{
		m_tx->draw ( 0, 0 );
	}


}	//namespace GAME

