#include "G_Font.h"

//-------------------------------------------------------------------------------------------------
// 定義
//-------------------------------------------------------------------------------------------------
namespace GAME
{
	//--------------------------------------------------
	//シングルトンパターン	static実体
	P_G_Font	G_Font::m_inst = nullptr;
	//--------------------------------------------------

	const int32 G_Font::FONT_SIZE = 20;

	G_Font::G_Font ()
	{
	}

	G_Font::~G_Font ()
	{
	}

	void G_Font::Load ()
	{
		m_font = s3d::Font ( FONT_SIZE );
	}


	void G_Font::Draw ( s3d::String str, double x, double y, s3d::ColorF clr ) const
	{
		m_font ( str ).draw ( x, y, clr );
	}


}	//namespace GAME

