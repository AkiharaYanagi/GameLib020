#include "G_Font.h"

//-------------------------------------------------------------------------------------------------
// 定義
//-------------------------------------------------------------------------------------------------
namespace GAME
{
	//--------------------------------------------------
	//シングルトンパターン	static実体
	P_G_Font	G_Font::m_inst = nullptr;
	//--------------------------------------------------

	//=======================================================================
	//	Rect 
	//=======================================================================
	G_Font::G_Font ()
	{
	}

	G_Font::~G_Font ()
	{
	}

	void G_Font::Load ()
	{
		m_font = s3d::Font ( m_size );
	}

	void G_Font::Draw ()
	{
		m_font ( m_str ).draw ( m_pos.x, m_pos.y, m_colorF );
	}

	void G_Font::SetPos ( VEC2 v )
	{
		m_pos = v;
	}

	void G_Font::SetStr ( s3d::String str )
	{
		m_str = str;
	}

	void G_Font::SetColorF ( s3d::ColorF clrf )
	{
		m_colorF = clrf;
	}


}	//namespace GAME

