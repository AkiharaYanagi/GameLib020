#include "Base_DebugOut.h"
#include "G_Font.h"


//-------------------------------------------------------------------------------------------------
// 定義
//-------------------------------------------------------------------------------------------------
namespace GAME
{
	//=====================================================
	//	基本情報表示
	//=====================================================

	Base_DebugOut::Base_DebugOut ()
	{
	}

	Base_DebugOut::~Base_DebugOut ()
	{
	}

	void Base_DebugOut::Draw ()
	{
		if ( ! m_valid ) { return; }

		G_FONT_DRAW ( G_Font::SIZE_20, m_str, m_pos.x, m_pos.y, s3d::ColorF { 1.0, 0.0, 1.0 } );
	}

	void Base_DebugOut::SetPos ( VEC2 v )
	{
		m_pos = v;
	}

	void Base_DebugOut::SetStr ( s3d::String str )
	{
		m_str = str;
	}


}	//namespace GAME

